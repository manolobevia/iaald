                </section>
                <p><a href="http://www.iaald.org/news/preparing-xiv-iaald-world-congress-2013-cornell-university"><img src="images/about-iaald-video.jpg" /></a><a class="links" href="http://iaald.org/"><abbr title="International Association of Agricultural Information Specialists">IAALD</abbr></a>'s mission is to enable our members to create, capture, access and disseminate information to achieve a more productive and sustainable use of the world's land, water, and renewable natural resources. To further this mission: IAALD connects agricultural information specialists worldwide, providing platforms and spaces for information dissemination, exchange and knowledge sharing; <abbr title="International Association of Agricultural Information Specialists">IAALD</abbr> convenes agricultural information specialists worldwide, organising meetings and catalyzing dialogue among all agricultural information stakeholders; <abbr title="International Association of Agricultural Information Specialists">IAALD</abbr> communicates and advocates the value of knowledge and information to its members and others, improving the status and practice of agricultural information management and dissemination; <abbr title="International Association of Agricultural Information Specialists">IAALD</abbr> collaborates with members and other partner organisations, facilitating and catalyzing educational and other opportunities across agricultural information communities.</p>
                <p><abbr title="International Association of Agricultural Information Specialists">IAALD</abbr> World Congresses are held every three to five years. Some of our previous congresses have been held in Montpelier, France (2010); Toyko, Japan (2008); Lexington, Kentucky, USA (2005); and Dakar, Senegal (2000).</p>
                <p><a class="links" href="http://mannlib.cornell.edu/">ALBERT R. Mann Library</a> collections and services support the faculty, staff and students of the University. In particular our resources and staff support the instruction, research, and extension programs of Cornell University's College of Agriculture and Life Sciences and College of Human Ecology. As a land grant library, Mann also supports the research needs of citizens of the state of New York and participates in the community of land grant libraries throughout the United States. In keeping with the Cornell University's international mission, Mann library promotes innovations in information systems and services for international development. Mann Library is committed to providing outstanding information and research services in the areas of importance to its constituents through expert staff, scholarly resources, state of the art information technology, strategic partnerships and continuous innovation.</p>
