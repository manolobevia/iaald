            <h3>
                Emerging Priorities for Scientific & <strong>Agricultural</strong> Information
            </h3>
            <p><abbr title="International Association of Agricultural Information Specialists">IAALD</abbr> World Congress 2013 at Cornell University is an opportunity for participants to explore, share, and discuss emerging priorities in discovery and access systems, content-management tools, and institutional or individual collaborations, in a content-rich era that is shaped by open-source technology, user-driven content, and partnerships,</p>
            <p>The three-day Congress will feature contributed paper discussions, panels with leading experts, and interactive workshops with plenty of time for networking with colleagues. </p>
              <p>Join librarians, IT professionals, and researchers from agricultural and related fields from across to discuss a broad, dynamic array of issues, such as: Capacity building, Collaboration and partnerships, Collection development, Data curation/management, Digital libraries and repositories, Discovery &amp; access systems, Distance learning, Electronic resources, Emerging technologies, Fundraising/development, Hot topics, Information literacy, Information Management, Information Access, Innovation, Intellectual property &amp; copyright, Leadership &amp; management, Metadata &amp; cataloging, Mobile technologies, Open source technology, Outreach/engagement, Professional/staff development, Scholarly communication, Social media (Facebook, Twitter, etc), Special collections, Teaching &amp; learning, Technology, and User driven content.</p>
